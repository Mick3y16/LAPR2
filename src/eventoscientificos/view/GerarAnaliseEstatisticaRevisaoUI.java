    public GerarAnaliseEstatisticaRevisaoUI(java.awt.Frame parent, boolean modal, Empresa empresa) {
        super(parent, modal);
        this.framePai = parent;
        this.controller = new GerarAnaliseEstatisticaRevisaoController(empresa);
        this.controller.gerarAnaliseEstatisticas();
        setResizable(false);
        getRootPane().setDefaultButton(this.btn_notificar);
        setLocationRelativeTo(null);
        initComponents();
        if (!empresa.isAdministrador(
                new Administrador(empresa.getUtilizadorAutenticado()))) {
            JOptionPane.showMessageDialog(
                    framePai,
                    "N찾o tem permiss천es para submeter artigos a partir de"
                    + "ficheiros.",
                    "Gerar Estatistica Revisor",
                    JOptionPane.ERROR_MESSAGE);
            dispose();
        } else if (this.controller.getResultadosFinais().length < 0) {
            JOptionPane.showMessageDialog(
                    framePai,
                    "N찾o existem submiss천es aceites!",
                    "Gerar Estatistica Revisor",
                    JOptionPane.ERROR_MESSAGE);
            dispose();

        } else {
            setVisible(true);
            pack();
        }
    }

